package br.com.generation.rafaela;

public class OperadorLogico {
	/* == --> igual
	 * !* --> diferente
	 * > --> maior
	 * >= --> maior igual
	 */
	public static void main(String[] args) {
		
		int idade = 17;
		
		if(idade <= 17)
		{
			System.out.println("Menor de dezoito");
			
		}
		else {
			System.out.println("Maior de dezoito");
		}
	
	}
		
}
